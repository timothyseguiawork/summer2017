# The goal isn't to get you to understand code, but to teach you the following 3 skills.
# 1 Finding Python source code for things you need.
# 2 Reading through the code and looking for files.
# 3 Trying to understand code you find.

# Here's what you do:
# 1 Go to bitbucket.org, github.com, or gitorious.org with your favorite web browser and search
#   for "python".
# 2 Pick a random project and click on it.
# 3 Click on the source tab and browse thorugh the list of files and directories until you find
#   a .py file.
# 4 Start at the top and read through the code, taking notes on what you think it does.
# 5 If any symbols or strange words seem to interest you, write them down to look for later.

